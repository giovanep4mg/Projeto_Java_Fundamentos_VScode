public class Numericos {
    public static void main(String[] args) throws Exception {
        
        /*  São operações matemáticas.
         * 
        */

        // criando as variáveis 
        int num1;
        int num2;
        int resultado;

        // passa os valores para as variáveis
        num1 = 10;
        num2 = 30;

System.out.println("============================ Resultado da Soma ================================");
        
        // fazer a soma das variáveis
        resultado = num1 + num2;

        System.out.println("O resultado da soma é: "+resultado);

System.out.println("============================ Resultado da Subtração ================================");

        // fazer a subtração das variáveis
        resultado = num1 - num2;

        System.out.println("O resultado da subtração é: "+resultado);

System.out.println("============================ Resultado da Divisão ================================");

        // fazer a Divisão das variáveis
        resultado = num1 / num2;

        System.out.println("O resultado da divisão é: "+resultado);

System.out.println("============================ Resultado da Multiplicação ================================");

        // fazer a Multiplicação das variáveis
        resultado = num1 / num2;

        System.out.println("O resultado da Multiplicação é: "+resultado);

System.out.println("================================== FIM =====================================");
        
    }
}
