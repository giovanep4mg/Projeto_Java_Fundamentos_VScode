

public class SWITCH {
    public static void main(String[] args) throws Exception {
        // Estruturas Condicionais são usadas para fazer verificações
        // SWITCH -> UMA CONDICIONAL DE ESCOLHA
        // Escolhe somente uma das opções -> case ?

        
System.out.println("====================== Condicionais Switch ==========================\n");
        // variáveis com seus valores
        int opcao = 3;

        // fazer a verificação se o número 1 é maior que o número 2
        switch(opcao){
            case 1:
                System.out.println("Feijão.");
            break;
            case 2:
                System.out.println("Macarrão.");
            break;
            case 3:
                System.out.println("Sopa.");
            break;
            case 4:
                System.out.println("Arroz.");
            break;
        }

        System.out.println();    

        
System.out.println("=======================================================================\n");


    }
}
